package CI346;

public class implicitConcurrency {
	
	public static  boolean calculatePrimes(int a) {
		if (a % 2 == 0) 
		{
			if (a == 2) 
			{
				return true;
			}
		return false;
		} else {
			for (int b = 3; b < a; b += 2) 
			{
				if (a % b == 0) 
				{
					return false;			
				}
			}
			return true;
		}
	}
}
